// Quiz
// What is the difference between var and let?
// The difference between var and let is that var has a function scope and let has a block scope.

// What is the difference between var and const?
// The difference between var and const is that var can be reassigned while const cannot. Const also has a block scope.

// What is the difference between let and const?
// The difference between let and const is that let can be reassigned a value but not redeclared while const cannot be reassigned nor redeclared.

// What is hoisting?
// When using "var" to declare and initialize variables, at compilation, the variables are declared before the code is run even if there are no values assigned to them until the code is run. Variable declarations made with the let and const keywords are not hoisted.
